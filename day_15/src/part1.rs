use crate::{parse, try_move};

pub fn process(input: &str) -> i32 {
    let (mut grid, steps) = parse(input);

    let mut robot = *grid
        .iter()
        .find_map(|(p, o)| match o {
            '@' => Some(p),
            _ => None,
        })
        .expect("Robot not found");

    for step in steps {
        if try_move(&mut grid, robot, step) {
            robot += step;
        }
    }

    grid.iter()
        .filter_map(|(p, c)| match c {
            'O' => Some(p.x + 100 * p.y),
            _ => None,
        })
        .sum()
}

